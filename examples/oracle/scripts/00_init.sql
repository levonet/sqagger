CREATE USER myuser IDENTIFIED BY secret;
ALTER SESSION SET CURRENT_SCHEMA=myuser;
ALTER USER myuser QUOTA 1000M ON system;
GRANT CREATE SESSION TO myuser;
GRANT CREATE TABLE TO myuser;

CREATE TABLE pet (
    pet_id NUMBER GENERATED BY DEFAULT AS IDENTITY,
    name VARCHAR2(50) NOT NULL,
    status VARCHAR2(10) CHECK(status IN ('available','pending','sold')) NOT NULL,
    PRIMARY KEY(pet_id)
);

COMMIT;
